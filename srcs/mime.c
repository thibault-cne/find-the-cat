char *get_mime_type(char *filename)
{
    char *ext;
    char *f_name;
    char *res;
    int i;

    f_name = filename;

    ext = strrchr(f_name, '.');
    if (!ext)
        return NULL;

    i = 0;

    while (Mime_types[i][0] != NULL)
    {
        if (!strcmp(Mime_types[i][0], ext))
        {
            // Get mime type
            res = (char *)malloc(sizeof(char) * (strlen(Mime_types[i][1]) + 1));
            strcpy(res, Mime_types[i][1]);
            return res;
        }
        i++;
    }

    return NULL;
}

